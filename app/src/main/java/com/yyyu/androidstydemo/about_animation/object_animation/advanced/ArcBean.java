public class ArcBean {

    private float angle;
    private float offset;

    public ArcBean() {

    }

    public ArcBean(float angle, float offset) {
        this.angle = angle;
        this.offset = offset;
    }

    public float getAngle() {
        return angle;
    }

    public void setAngle(float angle) {
        this.angle = angle;
    }

    public float getOffset() {
        return offset;
    }

    public void setOffset(float offset) {
        this.offset = offset;
    }
}
